class Codec:
    def serialize(self, root):
        """Encodes a tree to a single string.
        
        :type root: TreeNode
        :rtype: str
        """
        res = []
        def dfs(node):
            if node is None:
                res.append("N")
                return
            res.append(str(node.val))
            dfs(node.left)
            dfs(node.right)
        
        dfs(root)
        return ",".join(res)

    def deserialize(self, data):
        """Decodes your encoded data to tree.
        
        :type data: str
        :rtype: TreeNode
        """
        values = data.split(",")
        self.i = 0
        def dfs():
            if values[self.i] == "N":
                self.i += 1
                return None

            curr_node = TreeNode(int(values[self.i]))
            self.i += 1
            curr_node.left = dfs()
            curr_node.right = dfs()

            return curr_node
        
        return dfs()

